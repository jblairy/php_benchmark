<?php

declare(strict_types=1);

namespace Jblairy\PhpBenchmark\Infrastructure\Persistence\Doctrine\Entity;

use DateTimeImmutable;
use Doctrine\DBAL\Types\Types;
use Doctrine\ORM\Mapping as ORM;
use Jblairy\PhpBenchmark\Domain\PhpVersion\Enum\PhpVersion;

/**
 * Benchmark entity storing test code and metadata in database.
 */
#[ORM\Entity]
#[ORM\Table(name: 'benchmarks')]
#[ORM\Index(columns: ['category'], name: 'idx_category')]
#[ORM\Index(columns: ['slug'], name: 'idx_slug')]
#[ORM\HasLifecycleCallbacks]
class Benchmark
{
    private const int SLUG_MAX_LENGTH = 255;

    private const int NAME_MAX_LENGTH = 255;

    private const int CATEGORY_MAX_LENGTH = 100;

    private const int ICON_MAX_LENGTH = 10;

    #[ORM\Id]
    #[ORM\GeneratedValue]
    #[ORM\Column(type: Types::INTEGER)]
    /** @phpstan-ignore property.unusedType (ID is generated by Doctrine ORM) */
    private ?int $id = null;

    /**
     * @var array<string>
     */
    #[ORM\Column(type: Types::JSON)]
    private array $tags = [];

    /**
     * @var array<string> Array of PhpVersion enum values
     */
    #[ORM\Column(type: Types::JSON)]
    private array $phpVersions = [];

    #[ORM\Column(type: Types::DATETIME_IMMUTABLE)]
    private DateTimeImmutable $createdAt;

    #[ORM\Column(type: Types::DATETIME_IMMUTABLE)]
    private DateTimeImmutable $updatedAt;

    /**
     * @param array<string> $phpVersions
     * @param array<string> $tags
     *
     * @SuppressWarnings("PHPMD.ExcessiveParameterList") - Doctrine entity requires all properties in constructor
     */
    public function __construct(
        #[ORM\Column(type: Types::STRING, length: self::SLUG_MAX_LENGTH, unique: true)]
        private string $slug = '',
        #[ORM\Column(type: Types::STRING, length: self::NAME_MAX_LENGTH)]
        private string $name = '',
        #[ORM\Column(type: Types::STRING, length: self::CATEGORY_MAX_LENGTH)]
        private string $category = '',
        #[ORM\Column(type: Types::TEXT)]
        private string $description = '',
        #[ORM\Column(type: Types::TEXT)]
        private string $code = '',
        array $phpVersions = [],
        array $tags = [],
        #[ORM\Column(type: Types::STRING, length: self::ICON_MAX_LENGTH, nullable: true)]
        private ?string $icon = null,
        #[ORM\Column(type: Types::INTEGER, nullable: true)]
        private ?int $warmupIterations = null,
        #[ORM\Column(type: Types::INTEGER, nullable: true)]
        private ?int $innerIterations = null,
    ) {
        $this->phpVersions = array_values($phpVersions); // Ensure sequential array
        $this->tags = array_values($tags);
        $this->createdAt = new DateTimeImmutable();
        $this->updatedAt = new DateTimeImmutable();
    }

    #[ORM\PreUpdate]
    public function onPreUpdate(): void
    {
        $this->updatedAt = new DateTimeImmutable();
    }

    public function getId(): ?int
    {
        return $this->id;
    }

    public function getSlug(): string
    {
        return $this->slug;
    }

    public function getName(): string
    {
        return $this->name;
    }

    public function getCategory(): string
    {
        return $this->category;
    }

    public function getDescription(): string
    {
        return $this->description;
    }

    public function getCode(): string
    {
        return $this->code;
    }

    /**
     * @return array<string>
     */
    public function getTags(): array
    {
        return array_values($this->tags);
    }

    public function getIcon(): ?string
    {
        return $this->icon;
    }

    /**
     * @return string[] Array of PhpVersion enum values
     */
    public function getPhpVersions(): array
    {
        return $this->phpVersions;
    }

    /**
     * @return PhpVersion[]
     */
    public function getPhpVersionEnums(): array
    {
        return array_map(
            PhpVersion::from(...),
            $this->phpVersions,
        );
    }

    public function supportsPhpVersion(PhpVersion $phpVersion): bool
    {
        return in_array($phpVersion->value, $this->phpVersions, true);
    }

    public function getCreatedAt(): DateTimeImmutable
    {
        return $this->createdAt;
    }

    public function getUpdatedAt(): DateTimeImmutable
    {
        return $this->updatedAt;
    }

    public function setName(string $name): void
    {
        $this->name = $name;
    }

    public function setCategory(string $category): void
    {
        $this->category = $category;
    }

    public function setDescription(string $description): void
    {
        $this->description = $description;
    }

    public function setCode(string $code): void
    {
        $this->code = $code;
    }

    /**
     * @param array<string> $tags
     */
    public function setTags(array $tags): void
    {
        $this->tags = array_values($tags);
    }

    public function setIcon(?string $icon): void
    {
        $this->icon = $icon;
    }

    /**
     * @param array<string> $phpVersions
     */
    public function setPhpVersions(array $phpVersions): void
    {
        $this->phpVersions = array_values($phpVersions);
    }

    public function getWarmupIterations(): ?int
    {
        return $this->warmupIterations;
    }

    public function setWarmupIterations(?int $warmupIterations): void
    {
        $this->warmupIterations = $warmupIterations;
    }

    public function getInnerIterations(): ?int
    {
        return $this->innerIterations;
    }

    public function setInnerIterations(?int $innerIterations): void
    {
        $this->innerIterations = $innerIterations;
    }
}
